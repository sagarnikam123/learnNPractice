#	Which of the following is the best interpretation of the interval?
#	
######################################################################################################################

1	We are 95% confident that babies born to nonsmoker mothers are on average 0.05 to 0.58 pounds heavier at birth 
	than babies born to smoker mothers.
2	We are 95% confident that babies born to nonsmoker mothers are on average 0.05 to 0.58 pounds lighter at birth 
	than babies born to smoker mothers.
3	We are 95% confident that the difference in average weights of babies whose moms are smokers and nonsmokers 
	is between 0.05 to 0.58 pounds.
4	We are 95% confident that the difference in average weights of babies in this sample whose moms are smokers 
	and nonsmokers is between 0.05 to 0.58 pounds.

Answer - 1	We are 95% confident that babies born to nonsmoker mothers are on average 0.05 to 0.58 pounds heavier at birth 
			than babies born to smoker mothers.