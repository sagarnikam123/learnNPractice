1	There appears to be no trend in the number of girls born from 1940 to 2002.
2	There is initially an increase in the number of girls born, which peaks around 1960. After 1960 there is a
	decrease in the number of girls born, but the number begins to increase again in the early 1970s. Overall the
	trend is an increase in the number of girls born in the US since the 1940s.
3	There is initially an increase in the number of girls born. This number peaks around 1960 and then after 1960
	the number of girls born decreases.
