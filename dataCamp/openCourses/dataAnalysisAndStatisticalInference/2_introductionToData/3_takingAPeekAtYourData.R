# Taking a peek at your data

#######################################################################################################################
#
#	You can have a look at the first or last few entries (rows) of your data frame with the commands
#	head() and tail(), respectively.
#
#	Note that you could also look at all of the data frame at once by typing its name into the console.
#	However, since cdc has 20,000 rows, this would mean flooding the console screen. It's better to take small peeks
#	at the data with head(), tail() or the subsetting techniques that you'll learn in a moment.
#
#######################################################################################################################

# Print the head and tails of the data frame:
head(cdc)
tail(cdc)
