let year = 1967;
console.log(year); // 1967
console.log(++year); // 1968

let month = 11;
console.log(month++); // 11
console.log(month); // 12

let quantity = "1992";
console.log(quantity); // 1992
console.log(typeof(quantity)); // string
console.log(typeof(+quantity)); // number

let day = "-1993";
console.log(+day); // -1993
console.log(-day); // 1993
