# Arrange the Pikachi

#######################################################################################################################
#	
#	Yes. There are many Pikachi (plural for Pikachu) on campus. Ash's "Gotta Catch 'em All" and so he sets out to do
#	so. However, he wants to catch them in the increasing order of their fighting power. He wants you to order them 
#	in the order he wants. Being the faithful sidekick that you are, you do as he asks.
#	
#	To reiterate, given the fighting powers of N pikachi, you have to output their fighting powers in increasing order.
#	
#	Input
#	
#	The first line contains the integer T- the number of test cases in that file.
#	
#	The first line of each test case contains a single integer - N the number of pikachi.
#	
#	The next line contains the fighting powers of the N pikachi. The numbers are inputted on a single line and are 
#	space separated.
#	
#	Output
#	
#	For each test case, output a single line containing N space separated integers- the fighting powers of the 
#	pikachi in increasing order.
#	
#	Constraints
#	1<= T <= 10
#	1<= N <=105
#	1<= Fighting Power of a pikachu <=109
#	
#######################################################################################################################
#	Input
#	
#	2
#	5
#	1 2 1 3 4
#	6
#	3 34 12 1 3 2
#######################################################################################################################
#	Output
#	
#	1 1 2 3 4
#	1 2 3 3 12 34
#######################################################################################################################