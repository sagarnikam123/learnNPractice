#	Output:
#
#	If the given points do not form a triangle, print ‘Invalid’, if they do and the triangle contains the origin,
#	print ‘Yes’, and if the triangle does not contain the origin, print ‘No’ (without the single quotes).
#
#	Note: A triangle with origin lying on one of the sides is also said to be containing origin.
#
#######################################################################################################################
#	Input
#
#	1 0
#	5 0
#	5 3
#######################################################################################################################
