#	
#	Output:
#	
#	For every test case print the total number of magic numbers p such that m <= p <= n, 
#	test cases separated by an line.
#	
#######################################################################################################################
#	Input
#	
#	2
#	1 19
#	3 5
#######################################################################################################################
#	Output
#	
#	1
