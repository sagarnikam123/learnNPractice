// Go program to illustrate the use of keywords
package main
// Here package keyword is used to include in the program

// import keyword is used to import "fmt" in your package
import "fmt"

// func is used to create function
func main() {
	// Jere. var keyyword is used to create variables
	// Pname, Lname, Cname are the valid identifiers
	var Pname = "GeeksForGeeks"
	var Lname = "Go Language"
	var Cname = "Keywords"

	fmt.Printf("Portal name: %s", Pname)
	fmt.Printf("\nLanguage name: %s", Lname)
	fmt.Printf("\nChapter name: %s\n", Cname)
}
